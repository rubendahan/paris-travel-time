import { chromium } from 'playwright'
const browser = await chromium.launch()
const page = await browser.newPage({ viewport: { width: 1600, height: 900 } })
await page.goto('http://localhost:5173/', { waitUntil: 'networkidle', timeout: 60000 })
await page.waitForTimeout(1500)
const input = page.locator('input[placeholder*="Adresse"]')
const box = await input.boundingBox()
console.log('input box:', JSON.stringify(box))
await input.click()
await page.waitForTimeout(600)
console.log('markers after input click:', await page.locator('.leaflet-marker-icon').count())
// click just below the input (looks like part of the bar zone)
await page.mouse.click(box.x + box.width / 2, box.y + box.height + 8)
await page.waitForTimeout(600)
console.log('markers after near-miss click:', await page.locator('.leaflet-marker-icon').count())
await browser.close()
